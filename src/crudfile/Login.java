    private void loginButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_loginButtonMouseClicked

        String uname = usernameTextField.getText();
        String pass = String.valueOf(passTextField.getPassword());

        CrudFile cf = new CrudFile();
        if (!cf.checkAccount(uname, pass)) {
            JOptionPane.showMessageDialog(this, "Incorrect Username Or Password", "Login Failed", 2);

        } else {
            JOptionPane.showMessageDialog(this, "Log In Successfully");
            ListOfUsers list = new ListOfUsers();
            Login.this.setVisible(false);
            list.setVisible(true);
            list.showData();
            this.dispose();
        }
    }//GEN-LAST:event_loginButtonMouseClicked

