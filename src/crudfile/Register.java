    private void submitButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_submitButtonMouseClicked
        CrudFile cf = new CrudFile();
        String fname = fnameTextField.getText();
        String mname = mnameTextField.getText();
        String lname = lnameTextField.getText();
        String email = emailTextField.getText();
        String pass1 = String.valueOf(passTextField.getPassword());
        String repass1 = String.valueOf(repassTextField.getPassword());

        if (!pass1.equals(repass1)) {
            JOptionPane.showMessageDialog(this, "Password don't Match!");
        } else {
            try {
                cf.addUser(fname, mname, lname, email, pass1);
            } catch (IOException ex) {
                Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(this, "Adding " + fname + " to the File");
            Login log = new Login();
            Register.this.setVisible(false);
            log.setVisible(true);
        }
    }//GEN-LAST:event_submitButtonMouseClicked

